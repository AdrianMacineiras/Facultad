package ejercicio4;
 
public enum BolasBillar {
    BLANCA(0,ColorBola.BLANCO,TipoBolas.LISA),
    BOLA1(1,ColorBola.AMARILLO,TipoBolas.LISA),
    BOLA2(2,ColorBola.AZUL,TipoBolas.LISA),
    BOLA3(3,ColorBola.ROJO,TipoBolas.LISA),
    BOLA4(4,ColorBola.VIOLETA,TipoBolas.LISA),
    BOLA5(5,ColorBola.NARANJA,TipoBolas.LISA),
    BOLA6(6,ColorBola.VERDE,TipoBolas.LISA),
    BOLA7(7,ColorBola.GRANATE,TipoBolas.LISA),
    BOLA8(8,ColorBola.NEGRO,TipoBolas.LISA),
    BOLA9(9,ColorBola.AMARILLO,TipoBolas.RAYADA),
    BOLA10(10,ColorBola.AZUL,TipoBolas.RAYADA),
    BOLA11(11,ColorBola.ROJO,TipoBolas.RAYADA),
    BOLA12(12,ColorBola.VIOLETA,TipoBolas.RAYADA),
    BOLA13(13,ColorBola.NARANJA,TipoBolas.RAYADA),
    BOLA14(14,ColorBola.VERDE,TipoBolas.RAYADA),
    BOLA15(15,ColorBola.GRANATE,TipoBolas.RAYADA);
     
    private int numBola;
    private ColorBola col;
    private TipoBolas tip;    
     
    BolasBillar(int numBola, ColorBola col, TipoBolas tip) {
        this.numBola = numBola;
        this.col = col;
        this.tip = tip;
    }
     
    public int getNumBola(){
        return numBola;
    }
     
    public ColorBola getColorBola(){
        return col;
    }
     
    public TipoBolas getTipoBolas(){
        return tip;
    }
 
    enum TipoBolas {
        LISA,
        RAYADA;
    }
     
    enum ColorBola {
    BLANCO,
    AMARILLO,
    AZUL,
    ROJO,
    VIOLETA,
    NARANJA,
    VERDE,
    GRANATE,
    NEGRO,
    }
}